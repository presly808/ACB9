package ua.artcode.week2;


public class TestLoopsIntro{

    public static void main(String[] args) {

        // start
        // while(<exit condition>){
        //  action
        //  step
        // }
        //

        int i = 0; // start
        while(i < 10){ // way condition
            System.out.println("Hello"); // action
            i--; // step
        }

        // i++ => i = i+1 => i += 1
    }


}
